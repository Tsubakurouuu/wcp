public class Chapter06 {
    public static void main(String[] args) {
        int a = 15;
        int c;
        c = a + 3;  // c = 18
        System.out.println(c);
        c = a - 5;  // c = 10
        System.out.println(c);
        c = a * 2;  // c = 30
        System.out.println(c);
        c = a / 6;  // c = 2
        System.out.println(c);
        c = a % 4;  // c = 3
        System.out.println(c);
    }
}